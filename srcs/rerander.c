#include "../includes/miniRT.h"

void		rerander(t_data *data)
{
	int	x;
	int	y;

	y = 0;
	while (y < data->img.height)
	{
		x = 0;
		while (x <= data->img.width)
		{
		
			x++;
		}
		y++;
	}
}
